function LZ4Decompressor() {
	this.decompressBlock = function(input, output, inputLength, inputOffset, outputOffset) {
		inputOffset = inputOffset || 0;
		outputOffset = (outputOffset != null ? outputOffset : output.length);
		inputLength = (inputLength != null ? inputLength : input.length - inputOffset);

		var inputIndex = inputOffset;
		function decodeVariableLength(n) {
			var c = n + 240;
			while (c >= 255) {
				c = input[inputIndex++];
				n += c;
			}
			return n;
		}

		var outputIndex = outputOffset;
		var inputEnd = inputOffset + inputLength;
		while (inputIndex < inputEnd) {
			var token = input[inputIndex++] & 255;
			var end = outputIndex + decodeVariableLength(token >>> 4);
			while (outputIndex < end) {
				output[outputIndex++] = input[inputIndex++];
			}
			if (inputIndex < inputEnd) {
				var offset = (input[inputIndex + 0] & 255) | ((input[inputIndex + 1] & 255) << 8);
				inputIndex += 2;
				var end = outputIndex + decodeVariableLength(token & 15) + 4;
				while (outputIndex < end) {
					output[outputIndex] = output[outputIndex - offset];
					++outputIndex;
				}
			}
		}
		return outputIndex - outputOffset;
	};
}

function LZ4CompressorMMC() {
	var chainStart;
	var chainNext; 																										// Even positions = standard chain, odd positions = "gateways" to next MMC level (i.e. one character longer match guarantee)
	var globalPosition = 65536;																							// Start global position counter at 65536 so that the initial zeros in the hash tables will point before actual data.
	var hash = 0;																										// Rolling hash, maintained between calls. Will be "rolled out" completely if inputOffset goes back to zero.

	(function(){
		if (typeof Int32Array === 'undefined') {
			chainStart = new Array(65536);
			chainNext = new Array(65536 * 2);
			for (var i = 0; i < 65536; ++i) {
				chainStart[i] = 0;
				chainNext[i * 2 + 0] = 0;
				chainNext[i * 2 + 1] = 0;
			}
		} else {
			chainStart = new Int32Array(65536);
			chainNext = new Int32Array(65536 * 2);
		}
	})();

	this.compressBlock = function(input, output, inputLength, inputOffset, outputOffset) {
		inputOffset = inputOffset || 0;
		outputOffset = (outputOffset != null ? outputOffset : output.length);
		inputLength = (inputLength != null ? inputLength : input.length - inputOffset);

		var outputIndex = outputOffset;
		function encodeVariableLength(n) {
			if (n >= 15) {
				n -= 15;
				while (n >= 255) {
					output[outputIndex++] = 255;
					n -= 255;
				}
				output[outputIndex++] = n;
			}
		}

		var literalIndex = inputOffset;
		var inputStop = inputOffset + inputLength - 5;																	// -5 due to lz4 requirement: 1) The last 5 bytes are always literals.
		for (var inputIndex = (inputOffset < 9 ? -3 : inputOffset - 12); inputIndex < inputStop - 7; ++inputIndex) {	// Tricky offsets due to lz4 requirement: 2) The last match must start at least 12 bytes before end of block.
			hash = ((hash << 4) ^ (input[inputIndex + 3] & 255)) & 65535;												// Stupidly simple rolling "hash" of four bytes (with three bytes "lookahead"). A few collisions don't matter much for performance.
			var chainIndex = (globalPosition & 65535) * 2;
			chainNext[chainIndex] = chainStart[hash];																	// Extend the chain.
			chainNext[chainIndex + 1] = 0;

			if (inputIndex >= literalIndex) {																			// If inputIndex < literalIndex we are "skipping" replicated data (but we still need to maintain hash table).
				var bestMatchOffset = 0;
				var bestMatchEnd = inputIndex;
				var right = inputIndex;
				var level = inputIndex;
				var chainPosition = chainStart[hash];
				var lastLowerLevelIndex = chainIndex;
				var lastHigherLevelIndex = lastLowerLevelIndex + 1;														// Current input position is our first candidate for a gateway.
				var offset;
				while ((offset = globalPosition - chainPosition) <= inputIndex && offset <= 65535 && level < inputStop) {
					chainIndex = (chainPosition & 65535) * 2;
					if (input[level] !== input[level - offset]) {
						lastLowerLevelIndex = chainIndex;
						chainPosition = chainNext[chainIndex];
					} else {
						var left = right;
						while (right < inputStop && input[right] === input[right - offset]) {
							++right;
						}
						if (right > bestMatchEnd) {
							do {
								--left;
							} while (left > level && input[left] === input[left - offset]);
							if (left > level) {
								right = left;																			// Wasn't a match after all. Left of "middle" had a mismatch. Start comparing from this point in the next iteration.
							} else {
								bestMatchEnd = right;																	// We got a better match!
								bestMatchOffset = offset;
							}
						}

						var nextPosition = chainNext[chainIndex];
						if (lastHigherLevelIndex >= 0) {																// Looking for next gateway, "promoting" matches > level along the way.
							chainNext[lastHigherLevelIndex] = chainPosition;
							chainNext[lastLowerLevelIndex] = nextPosition;
							lastHigherLevelIndex = chainIndex;
							if (chainNext[chainIndex + 1] !== 0) {														// Found the old gateway, remove it.
								nextPosition = chainNext[chainIndex + 1];
								++level;
								chainNext[chainIndex] = nextPosition;
								chainNext[chainIndex + 1] = 0;
								lastLowerLevelIndex = chainIndex;
								lastHigherLevelIndex = -1;																// We could actually turn this into a gateway to the next level directly if min match >= this next level, but it isn't worth the additional complexity.
							}
						} else {
							lastLowerLevelIndex = chainIndex;
							if (chainNext[chainIndex + 1] !== 0) {														// Is already a gateway to the next level, take it.
								nextPosition = chainNext[chainIndex + 1];
								++level;
							} else {
								lastHigherLevelIndex = chainIndex + 1;													// Turn into a gateway to the next level
							}
						}
						chainPosition = nextPosition;
					}
				}
				if (lastHigherLevelIndex >= 0) {
					chainNext[lastHigherLevelIndex] = chainPosition;
				}

				var matchLength = bestMatchEnd - inputIndex;
				if (matchLength >= 4) {																					// lz4 requires a minimum of four bytes for a copy
					var literalLength = inputIndex - literalIndex;
					output[outputIndex++] = ((literalLength < 15 ? literalLength : 15) << 4)
							| (matchLength < 19 ? (matchLength - 4) : 15);
					encodeVariableLength(literalLength);
					while (literalIndex < inputIndex) {
						output[outputIndex++] = input[literalIndex++];
					}
					literalIndex += matchLength;
					output[outputIndex++] = bestMatchOffset & 255;
					output[outputIndex++] = (bestMatchOffset >>> 8) & 255;
					encodeVariableLength(matchLength - 4);
				}
			}
			chainStart[hash] = globalPosition;																			// Store absolute "global" position in the hash table. Garbage hashes when inputIndex is negative doesn't matter since they will never be used.
			++globalPosition;
		}

		var literalLength = inputStop + 5 - literalIndex;																// End with a literal of at least 5 bytes.
		output[outputIndex++] = ((literalLength < 15 ? literalLength : 15) << 4);
		encodeVariableLength(literalLength);
		while (literalIndex < inputStop + 5) {
			output[outputIndex++] = input[literalIndex++];
		}
		return outputIndex - outputOffset;
	};
}

